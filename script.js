select1.addEventListener("click",(e)=>{
    let fc=select1.value;    

    if(fc=="EUR"){
        img1.src="https://www.usflagsupply.com/images/detailed/18/europe-flag_uh4m-w8.jpg";
    }else{
    img1.src=`https://flagsapi.com/${fc.slice(0,2)}/flat/64.png`;
    }
});


