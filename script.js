async function getExChangeRate(){
    let from=select1.value.toLowerCase();
    let url=`https://cdn.jsdelivr.net/npm/@fawazahmed0/currency-api@latest/v1/currencies/${from.toLowerCase()}.json`;
    let response= await fetch(url);
    let data= await response.json();
    let to=select2.value.toLowerCase();
    let v=data[from][to];
    let inputamount = input.value;
    return v*inputamount;
}